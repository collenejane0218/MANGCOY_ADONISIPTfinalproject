        <img src="IMAGES/gasangasang.jpg" alt="GASANG-GASANG FESTIVAL GASAN MARINDUQUE">
        <p>GASAN, Marinduque (PIA) -- After a successful Agro-tourism trade fair and food bazaar launching, the local government of Gasan revived the Gasang-gasang Street Dance Festival on Easter Sunday.</p>

        <p>Among the contingents who participated were Behold the Risen Lord Cultural Group, Renovador en La Kultura, Nuestra Salvador and Sitio Tanig-Babayanan Cultural Group which consisted of different barangays in the town.</p>

        <p>In his message, Mayor Rolando Tolentino  expressed his gratitude to the participating groups who took an extra mile in preparing for the activity, from the props creation to the dance execution.</p>

        <p>"Ako po ay masaya, lalo’t higit na nasisimulan na nating maibalik ang Gasang-gasang sa ating lugar na kahit po tayo ay nakapaglaan lamang ng maliit na pondo para rito ay hindi tayo tinanggihan ng ating mga barangay na sumali sa patimpalak na ito (I am happy, especially since we are starting to bring back the Gasang-gasang in our town that even though we only allocated a small budget for this, our barangays did not hesitate to participate in this contest,” Tolentino said.</p>

        <p>After witnessing the contingents showcase their talents, Nuesta Salvador Cultural Group was declared first place followed by Renovador En Kultura. Sitio Tanig-Babayanan received the third place while Behold the Risen Lord Cultural Group was awarded fourth.</p>

        <p>Gasang-gasang Festival is a yearly colorful Easter Sunday celebration. It commemorates the town’s origin name which is a type of coral that can be seen in its shoreline. (RAMJR/AMKDA/PIA-Marinduque)</p>
        <p>A street dancing festival in Gasan town similarly inspired by the Moriones and the Sinakulo, passion plays. Accompanied by the beat of festival drums, contingents from the different barangays compete for the best in costume and choreography. The drama revolves around the morion tradition and the Resurrection and takes place on Easter Sunday</p>

