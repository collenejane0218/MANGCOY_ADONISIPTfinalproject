        <h2>Basud Hidden Falls</h2>
        <p>In the advent of social media and effect of COVID-19 pandemic where freedom of movement has been limited, the so-called sleeping wonders and best-kept secrets of mostly every town suddenly propelled to the forefront of the travel and tourism scene and Basud Hidden Falls as well as Balugbog Baboy Mini Falls are prime examples.  Now, it is one of the most sought after destinations in Torrijos for nature-lovers, adventure-seekers and waterfalls-chasers alike.</p>

         <p>This is newly discovered gem in Torrijos and its popularity is starting to gain buzz. Not just one, not two, but three waterfalls are waving. It is best experience after rain to witness the gushing water, but make sure to wait for a few days to give the trail a time to dry up and to avoid a muddy and slippery trek.</p>

         <p>You could also have the chance to drop by at the barangay’s view deck where you can unwind and appreciate a relaxing view of nature, overlooking the neighboring island provinces of Romblon, Mindoro, and Quezon. If you an adventure enthusiast, then exploring Basud Hidden Falls of Payanas is the right place for you. The locals are very helpful, hospitable, and very warm hearted to welcome and assist you.</p>

