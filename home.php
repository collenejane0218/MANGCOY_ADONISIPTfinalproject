          <h2>WELCOME TO GASAN MARINDUQUE</h2>
          <img src="IMAGES/gasan.jpg" alt="Description of the image" width=700" height="500">
        

         <p>Gasan is home to some of Marinduque's best-known tourist spots:</p>

        <p>Guingona Park: This park located in Barangay Uno is where Gasan's cenaculos are played during the Moriones Festival.</p>
          <img src="IMAGES/PARK.jpg" alt="Description of the image" width=700" height="500">

        <p>Reyes Park: Reyes Park in Barangay Dos serves as the Gasan boardwalk. This park is about to demolish as the present administration under Victoria Lao Lim pursue her intention of building a Commercial Sports Complex that according to her may generate funds for GASUAF. Fairs are conducted in the park, and a wooden footbridge leading to nearby Barangay Dili can be seen (and crossed). The bridge is a landmark of Barangay Dili.</p>
        <img src="IMAGES/SEAVIEW.jpg" alt="Description of the image" width=700" height="500">

        <p>Talao Cave: Talao Cave in Barangay Tiguion is a series of twelve caves accessible from three mountain trails that can only be crossed on foot. The caves are located in the midst of a rain forest.</p>
        <img src="IMAGES/TALAO.jpg" alt="Description of the image" width=700" height="500">

        <p>Tres Reyes Islands: The Tres Reyes Islands in Barangay Pinggan, named after the Three Kings of the Nativity, are a potential tourist destination. True to its name, the island chain consists of three islands named Gaspar, Melchor and Baltazar and are visible from any point of the Gasan coastline. Reaching the islands takes about 30–45 minutes by boat</p>
