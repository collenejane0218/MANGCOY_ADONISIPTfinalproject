         <img src="IMAGES/makulilis.jpg" alt="MAKULILIS PEAK TORRIJOS MARINDUQUE" width="=500" height="300">
         <p> From an old Jesuit note of P. Juan de Noceda and from Vocabulario de la Lengua Tagala, from 1832, it said that the place is named Ma-culiglig. In restrospect, “Culiglig” is a tagalog term for crickets, thus Makulilis, onomatopoeic in nature. Further checking out the description from that period, they define “culiglig” as a slang, derogatory term for rumor spreaders and gossipers. “Culiglig n. – Chicharra se dice de una mugger habladora” (Culiglig noun. – gossip girl).</p>
